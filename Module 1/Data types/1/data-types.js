// String
const string = 'text'
console.log('string', string, typeof string)
// Number
const number = 1
console.log('number', number, typeof number)
// Boolean
const boolean = true
console.log('boolean', boolean, typeof boolean)
// Null
const nullName = null
console.log('nullName', nullName, typeof nullName)
// Undefined
const undefinedName = undefined
console.log('undefinedName', undefinedName, typeof undefinedName)
// Object
const obj = {one: 1, two: 2}
console.log('obj', obj, typeof obj)
// Symbol
const symb = Symbol('example')
console.log('symb', symb, typeof symb)
// BigInt
const bigInt = 12345678n
console.log('bigInt', bigInt, typeof bigInt)

